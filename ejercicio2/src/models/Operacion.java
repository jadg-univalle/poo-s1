package models;

public class Operacion {

    public int sumar(int num1, int num2){
        return num1 +  num2;
    }

    public int restar(int num1, int num2){
        return num1 - num2;
    }

    public int multiplicar(int num1, int num2){
        return num1 * num2;
    }

    public double dividir(double num1, double num2){
        return num1 / num2;
    }

}
